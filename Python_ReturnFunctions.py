def GreaterNumber(num1, num2):
    if(num1>num2):  # if num1 is more then num2
        return num1  # return if num1 is more then num 2
    else:  # if num1 isnt more then num2
        return num2  # return num2 if more then num1

def SmallerNumber(num3, num4):
    if(num3<num4):  # if num3 is less than num4
        return num3  # return num3
    else:  # if num3 is not less then num4
        return num4  # return num4